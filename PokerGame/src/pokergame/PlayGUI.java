    private Icon picMatcher(Card cd)
    {
        if(cd instanceof Club)
        {
            Icon type = new ImageIcon(getClass().getResource("/image/club.png"));
            System.out.println("A card in your hand said \"I'm a Club\"");
            return type;
        }        
        else if(cd instanceof Heart)
        {
            Icon type = new ImageIcon(getClass().getResource("/image/heart.png"));
            System.out.println("A card in your hand said \"I'm a Heart\"");
            return type;
        }
        else if(cd instanceof Spade)
        {
            Icon type = new ImageIcon(getClass().getResource("/image/spade.png"));
            System.out.println("A card in your hand said \"I'm a Spade\"");
            return type;
        }
        else if(cd instanceof Diamond)
        {
            Icon type = new ImageIcon(getClass().getResource("/image/diamond.png"));
            System.out.println("A card in your hand said \"I'm a Diamond\"");
            return type;
        }
        else
        {
            System.out.println("I'm nothing");
            return null;
        }
    }
    
