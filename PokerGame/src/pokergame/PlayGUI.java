    private void action_standActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_action_standActionPerformed
        // TODO add your handling code here:
        System.out.println("Current bot sum is: "+bot.HandRankSum());
        status.setText("Player hand sum is: "+p1.HandRankSum()+". House hand sum is: "+bot.HandRankSum()+".");
        ai_rank_1.setVisible(true);
        ai_hand_1.setIcon(picMatcher(bot.seeHand(0)));
        
        do{
            defaultDealer(bot);            
        }while(bot.HandRankSum()<17);
        
        System.out.println("Bot sum after greater than 17 is"+bot.HandRankSum());
        if(bot.HandRankSum()<21 || bot.HandRankSum()==21)
        {
            if(p1.HandRankSum()>bot.HandRankSum())
            {
                System.out.println("Current bot sum is: "+bot.HandRankSum());
                status.setText("Player hand sum is: "+p1.HandRankSum()+". House hand sum is: "+bot.HandRankSum()+".");
                JOptionPane.showMessageDialog(null, "Player Win", "Result", JOptionPane.PLAIN_MESSAGE);
                endGameDisable();
            }
            else if(p1.HandRankSum()<bot.HandRankSum())
            {
                System.out.println("Current bot sum is: "+bot.HandRankSum());
                status.setText("Player hand sum is: "+p1.HandRankSum()+". House hand sum is: "+bot.HandRankSum()+".");
                JOptionPane.showMessageDialog(null, "House Win", "Result", JOptionPane.PLAIN_MESSAGE);
                endGameDisable();
            }
            else
            {
                System.out.println("Current bot sum is: "+bot.HandRankSum());
                status.setText("Player hand sum is: "+p1.HandRankSum()+". House hand sum is: "+bot.HandRankSum()+".");
                JOptionPane.showMessageDialog(null, "Push", "Result", JOptionPane.PLAIN_MESSAGE);
                endGameDisable();
            }
        }            
        else
        {
            System.out.println("Current bot sum is: "+bot.HandRankSum());
            status.setText("Player hand sum is: "+p1.HandRankSum()+". House hand sum is: "+bot.HandRankSum()+".");
            JOptionPane.showMessageDialog(null, "House Bust, Player Win", "Result", JOptionPane.PLAIN_MESSAGE);
            endGameDisable();
        }
    }//GEN-LAST:event_action_standActionPerformed

